package entidad;

public class ListadoEquipo {
	
	private String idEquipo; 
	private String nombreEquipo; 
	private String descripcionEquipo; 
	private String idUnidadOrganica;
	private int cantidadCargo;
	
	
	public String getIdEquipo() {
		return idEquipo;
	}
	public void setIdEquipo(String idEquipo) {
		this.idEquipo = idEquipo;
	}
	public String getNombreEquipo() {
		return nombreEquipo;
	}
	public void setNombreEquipo(String nombreEquipo) {
		this.nombreEquipo = nombreEquipo;
	}
	public String getDescripcionEquipo() {
		return descripcionEquipo;
	}
	public void setDescripcionEquipo(String descripcionEquipo) {
		this.descripcionEquipo = descripcionEquipo;
	}
	public String getIdUnidadOrganica() {
		return idUnidadOrganica;
	}
	public void setIdUnidadOrganica(String idUnidadOrganica) {
		this.idUnidadOrganica = idUnidadOrganica;
	}
	public int getCantidadCargo() {
		return cantidadCargo;
	}
	public void setCantidadCargo(int cantidadCargo) {
		this.cantidadCargo = cantidadCargo;
	}
	
	
	
	
	
	
	
	
	
}
